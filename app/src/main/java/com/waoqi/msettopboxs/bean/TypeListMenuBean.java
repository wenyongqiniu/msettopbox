package com.waoqi.msettopboxs.bean;

public class TypeListMenuBean {
    private int id;
    private String name;

    public TypeListMenuBean(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
